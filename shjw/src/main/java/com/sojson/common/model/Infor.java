package com.sojson.common.model;

import java.io.Serializable;

/**
 * @author xj
 * @version 1.0 2017-11-30 留言实体
 */
public class Infor implements Serializable {
	private static final long serialVersionUID = 1L;
	private int id;// 主键
	private String contact_name;// 联系人姓名
	private String contact_phone;// 联系人电话
	private String infor;// 留言内容
	private String insert_time;// 添加时间
	private String openid;// 用户openid
	private int state;// 状态 默认为0表示未处理 1表示已处理
	private String update_time;// 修改时间
	private String operator;// 操作者

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getContact_name() {
		return contact_name;
	}

	public void setContact_name(String contact_name) {
		this.contact_name = contact_name;
	}

	public String getContact_phone() {
		return contact_phone;
	}

	public void setContact_phone(String contact_phone) {
		this.contact_phone = contact_phone;
	}

	public String getInfor() {
		return infor;
	}

	public void setInfor(String infor) {
		this.infor = infor;
	}

	public String getInsert_time() {
		return insert_time;
	}

	public void setInsert_time(String insert_time) {
		this.insert_time = insert_time;
	}

	public String getOpenid() {
		return openid;
	}

	public void setOpenid(String openid) {
		this.openid = openid;
	}

	public int getState() {
		return state;
	}

	public void setState(int state) {
		this.state = state;
	}

	public String getUpdate_time() {
		return update_time;
	}

	public void setUpdate_time(String update_time) {
		this.update_time = update_time;
	}

	public String getOperator() {
		return operator;
	}

	public void setOperator(String operator) {
		this.operator = operator;
	}

}
