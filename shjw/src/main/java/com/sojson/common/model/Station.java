package com.sojson.common.model;

import java.io.Serializable;

/**
 * 检测站实体
 * 
 * @author zjf
 * @version 1.0,2017年8月21日
 * 
 */
public class Station implements Serializable {
	private static final long serialVersionUID = 1L;
	private int stationid;
	private String station_no;// 检测站编号
	private String station_name;// 检测站名字
	private String station_address;// 检测站地址
	private String station_phone;// 检测站电话
	private String station_area;// 检测站区域
	private String insert_time; // 添加时间
	private String station_province;// 省份

	public Station() {
		super();
	}

	public Station(String station_no, String station_name,
			String station_address, String station_phone, String station_area,
			String insert_time, String station_province) {
		super();
		this.station_no = station_no;
		this.station_name = station_name;
		this.station_address = station_address;
		this.station_phone = station_phone;
		this.station_area = station_area;
		this.insert_time = insert_time;
		this.station_province = station_province;
	}

	public int getStationid() {
		return stationid;
	}

	public void setStationid(int stationid) {
		this.stationid = stationid;
	}

	public String getStation_no() {
		return station_no;
	}

	public void setStation_no(String station_no) {
		this.station_no = station_no;
	}

	public String getStation_name() {
		return station_name;
	}

	public void setStation_name(String station_name) {
		this.station_name = station_name;
	}

	public String getStation_address() {
		return station_address;
	}

	public void setStation_address(String station_address) {
		this.station_address = station_address;
	}

	public String getStation_phone() {
		return station_phone;
	}

	public void setStation_phone(String station_phone) {
		this.station_phone = station_phone;
	}

	public String getStation_area() {
		return station_area;
	}

	public void setStation_area(String station_area) {
		this.station_area = station_area;
	}

	public String getInsert_time() {
		return insert_time;
	}

	public void setInsert_time(String insert_time) {
		this.insert_time = insert_time;
	}

	public String getStation_province() {
		return station_province;
	}

	public void setStation_province(String station_province) {
		this.station_province = station_province;
	}

}
