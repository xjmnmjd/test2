package com.sojson.common.model;
/**
 * 修改记录实体
 * 
 * @author zjf
 * @version 1.0,2018年2月23日
 * 
 */
public class ChangeRecord {
   private int id;
   private int changeType;//修改类型 1-删除订单  2-删除应付总额 
   //3-删除线下支付 4-删除支出  5-修改线下支付 6-修改支出 7-修改应付总额
   private String changeCause;//修改原因
   private String operator;//操作者
   private String orderno;//订单号
   private String openid;
   private int userid;  //操作者id
   private String update_time;//更改时间
   private String licenseplate;//车牌
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getChangeType() {
	return changeType;
}
public void setChangeType(int changeType) {
	this.changeType = changeType;
}
public String getChangeCause() {
	return changeCause;
}
public void setChangeCause(String changeCause) {
	this.changeCause = changeCause;
}
public String getOperator() {
	return operator;
}
public void setOperator(String operator) {
	this.operator = operator;
}
public String getOrderno() {
	return orderno;
}
public void setOrderno(String orderno) {
	this.orderno = orderno;
}
public String getOpenid() {
	return openid;
}
public void setOpenid(String openid) {
	this.openid = openid;
}
public int getUserid() {
	return userid;
}
public void setUserid(int userid) {
	this.userid = userid;
}
public String getUpdate_time() {
	return update_time;
}
public void setUpdate_time(String update_time) {
	this.update_time = update_time;
}
public String getLicenseplate() {
	return licenseplate;
}
public void setLicenseplate(String licenseplate) {
	this.licenseplate = licenseplate;
}
   
}
